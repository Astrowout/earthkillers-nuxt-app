export default {
	// Props
	// -------------------------------------------------------------------------
	props: {
		url: {
			type: String,
			required: false,
		},
	},

	computed: {
		isExternalLink() {
			const regex = /^https?:\/\//i;

			return regex.test(this.url);
		},
	},
};
